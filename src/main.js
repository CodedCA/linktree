var r = document.querySelector(':root');

function pink() {
    r.style.setProperty('--main-color', 'rgb(190, 128, 149)');
}

function green() {
    r.style.setProperty('--main-color', 'rgb(128, 190, 141)');
}

function blue(){
    r.style.setProperty('--main-color', 'rgb(128, 145, 190)');
}